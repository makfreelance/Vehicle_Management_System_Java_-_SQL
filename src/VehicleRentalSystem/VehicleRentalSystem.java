    private void customerAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_customerAddActionPerformed
       
        String ID = jtxtID.getText();
        String Name = jtxtName.getText();
        String Surname = jtxtSurname.getText();
        String Address = jtxtAddress.getText();
        String VehicleNumber = jtxtRentedVehicleNum.getText();  // Adjusted to match table column
        
        
        try {
            Connection customerCon = ConnectionProvider.getCon();
            Connection vehicleCon = ConnectionProvider.getCon2();
            String query = "INSERT INTO customer_details (ID, Name, Surname, Address, VehicleNumber) VALUES (?, ?, ?, ?, ?)";
            PreparedStatement pst = customerCon.prepareStatement(query);
            
            pst.setString(1, ID);
            pst.setString(2, Name);
            pst.setString(3, Surname);
            pst.setString(4, Address);
            pst.setString(5, VehicleNumber);

            pst.executeUpdate();
            
            String select = "SELECT * FROM vehicles.vehicles WHERE VehicleNumber = ?";
            PreparedStatement pstSelect = customerCon.prepareStatement(select);
            pstSelect.setString(1, VehicleNumber);
            ResultSet rs = pstSelect.executeQuery();
            
            if(rs.next()){
                String updateQuery = "UPDATE vehicles.vehicles SET Status = 'Unavailable' WHERE VehicleNumber = ?";
                PreparedStatement pstUpdate = vehicleCon.prepareStatement(updateQuery);
                pstUpdate.setString(1, VehicleNumber);
                
                int rowsAffected = pstUpdate.executeUpdate();
                
                if (rowsAffected > 0) {
                    System.out.println("Status updated to 'Unavailable' for vehicle number: " + VehicleNumber);
                } else {
                    System.out.println("No update was made.");
                }
            }
            fetchAndDisplayCustomerData();    
            JOptionPane.showMessageDialog(null, "Customer successfully added.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_customerAddActionPerformed

    private void vehicleAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_vehicleAddActionPerformed
        String vehicleNumber = jtxtAddVehicleNum.getText();
        String vehicleName = jtxtVehicleName.getText();
        String colour = jtxtColour.getText();
        String status = "Available";
        
        try{
            Connection vehicleCon = ConnectionProvider.getCon2();
            
            String check = "SELECT * FROM vehicles.vehicles WHERE VehicleNumber = ?";
            PreparedStatement pstCheck = vehicleCon.prepareStatement(check);
            pstCheck.setString(1, vehicleNumber);
            ResultSet rs = pstCheck.executeQuery();
            
            if(rs.next()){
                String change = "UPDATE vehicles.vehicles SET Status = 'Availabe' WHERE VehicleNumber = ?";
                PreparedStatement pstChange = vehicleCon.prepareStatement(change);
                pstChange.setString(1, vehicleNumber);
                
                int rowsAffected = pstChange.executeUpdate();
                
                if (rowsAffected > 0) {
                    System.out.println("Status updated to 'Available' for vehicle number: " + vehicleNumber);
                } else {
                    System.out.println("No update was made.");
                }
            }else {
                    
                String query = "INSERT INTO vehicles (vehicleNumber, vehicleName, colour, status) VALUES (?, ?, ?, ?)";
                PreparedStatement pst = vehicleCon.prepareStatement(query);

                pst.setString(1, vehicleNumber);    // Set value for first '?'
                pst.setString(2, vehicleName);         // Set value for second '?'
                pst.setString(3, colour);   // Set value for third '?'
                pst.setString(4, status);

                pst.executeUpdate();            // Run the query

             }
            
            fetchAndDisplayVehicleData();
            JOptionPane.showMessageDialog(null, "Successfully Updated");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Student id already exists");
            
        }
    }//GEN-LAST:event_vehicleAddActionPerformed

