    private void vehicleDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_vehicleDeleteActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        int selectedRow = jTable2.getSelectedRow();
        
        if(selectedRow != -1){
            try {
                String id = (String) model.getValueAt(selectedRow, 0);
                Connection vehicleCon = ConnectionProvider.getCon2();
                PreparedStatement query = vehicleCon.prepareStatement("DELETE FROM vehicles WHERE VehicleNumber = ?");
                
                query.setString(1, id);
                
                query.executeUpdate();
                
            }catch (SQLException e){
                JOptionPane.showMessageDialog(null, "No row found with that ID.");
            }
            model.removeRow(selectedRow);
            JOptionPane.showMessageDialog(null, "Row deleted successfully.");  
        }else {
            JOptionPane.showMessageDialog(null, "Please select a row to delete.");
        }
    }//GEN-LAST:event_vehicleDeleteActionPerformed

